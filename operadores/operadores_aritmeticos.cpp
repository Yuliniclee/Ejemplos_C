#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;

    cout << "Suma: " << a + b << endl;
    cout << "Resta: " << a - b << endl;
    cout << "Multiplicacion: " << a * b << endl;
    cout << "Division: " << a / b << endl;
    cout << "Modulo: " << a % b << endl;

    return 0;
}
